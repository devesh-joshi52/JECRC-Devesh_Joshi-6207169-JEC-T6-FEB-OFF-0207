## INHERITANCE

'''
1. Single Level
2. Multi Level
3. Multiple
4. Herarchical
5. Hybrid
'''

## --Single Level--
# We will have a single parent and child class. Also the properties will be derived only one time. 

# Child Class or Sub Class 
# The class in which we are going to derive the properties, is known as Child class



## --Multi Level--
# It is a type of inheritance in which the properties will be derived from one to another class
# by consideringmore then one level


## --Multiple--
# It is a type of inheritance in which the properties will be derived from multiple parent class to single child class


## --Herarchical--
# It is a type of inheritance in which the properties will be derived from single parent class to multiple than one child class


## --Hybrid--
# It is a mixture of more than one type of inheritance